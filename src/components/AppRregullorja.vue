            <li>Testet e përfunduara të garuesve i dorëzon Kryetarit të komisionit të garave shtetërore.</li>
            <li>Gjatë mbajtjes së garave komunikon me personat përgjegjës duke siguruar mbarëvajtjen e garave.</li>    
        </ul>
    </div>
    <div class="text">
