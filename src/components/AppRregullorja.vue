        eksperimentale me kohëzgjatje të njëjtë.
        Gjatë 15 minutave nga fillimi i garave ose 15 minutave deri në përfundimin
        garave, garuesi nuk mund të largohet nga klasa në të cilën zhvillohet gara.
    </div>
